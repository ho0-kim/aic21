tracks = json.load(open('data/test-tracks.json', 'r'))
uuids = list(tracks.keys())

out_file = 'data/test-trafficlight_mrcnn.json'
out = {}
for i, uuid in enumerate(uuids):
    print(i, uuid)
    frame = tracks[uuid]["frames"][0]
    image = skimage.io.imread(frame)
    result = model.detect([image], verbose=0)
    try:
        idx = result[0]['class_ids'].tolist().index(10)
    except ValueError:
        out[uuid] = False
        continue
    # out[uuid] = result[0]['scores'][idx] > 0.95
    if result[0]['scores'][idx] > 0.98:
        out[uuid] = True
    else:
        out[uuid] = False
json.dump(out, open(out_file, 'w'), indent=4)